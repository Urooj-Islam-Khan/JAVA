public class UsingEscapeSequences {
    public static void main(String[] args) {
        System.out.println("I'm \'Urooj Islam\' \n Studen of software Engineering");
        //  \n is used for new line \' for single quote
        System.out.println("\"Coding is \t fun\"..\b");
        // \" used for double quote,  \t for tab
    }
}
